import cv2
import numpy

"""
    获取并修改像素值
    
    读取一幅图像，根据像素的行和列的坐标获取它的像素值
    对于RGB图像而言，返回RGB的值，
    对于灰度图则返回灰度值
"""
img = cv2.imread('1.jpg')
px = img[100, 100]  # 100行100列的像素值
print(px)

blue = img[100, 100, 0]  # 获取100行100列的B通道的像素值（blue值）
print(blue)

img[101, 101] = [255, 255, 255]  # 101行101列的像素值被赋值为[255,255,255]
print(img[101, 101])
